    public function addGroupModules()
    {
        $validator = validator(request()->all(), [
            'permission_group_id' => 'required|integer',
            'modules' => 'required|array',
            'modules.*.module_name' => 'required|string',
            'modules.*.permission_type' => 'required|string|in:full,read,hide',
            'users_id' => 'nullable|array',
            'users_id.*' => 'nullable|integer|exists:users,id'
        ]);
        if ($validator->fails()) {
            return $this->sendError($validator->errors());
        }
        $group = PermissionGroup::findOrFail(request('permission_group_id'));
        $group->createLog('updated');
        DB::beginTransaction();
        try {
            $check = DB::table('permission_group_users')->whereIn('user_id', request('users_id'))
                ->where('permission_group_id', '!=', request('permission_group_id'))->first(['id', 'user_id']);
            if ($check) {
                $fullname = User::where('id', $check->user_id)->value('fullname');
                DB::rollBack();
                return $this->sendError('Basqa qrupda movcuddur. [' . $fullname . ']');
            }
            $group->users()->sync(request('users_id'));
            $group->syncModule(request('modules'));
            DB::commit();
            return $this->sendSuccess();
        } catch (\Exception $ex) {
            DB::rollBack();
            return $this->sendError($ex->getMessage());
        }
    }

